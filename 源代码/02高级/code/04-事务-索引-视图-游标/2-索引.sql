--索引：提高检索查询效率
--SQL SERVER中有多种索引类型。
--按存储结构区分：“聚集索引（又称聚类索引，簇集索引）”，“非聚集索引（非聚类索引，非簇集索引）”
--按数据唯一性区分：“唯一索引”，“非唯一索引”
--按键列个数区分：“单列索引”，“多列索引”。
--聚集索引：根据数据行的键值在表或视图中的排序存储这些数据行，每个表只有一个聚集索引。
--聚集索引是一种对磁盘上实际数据重新组织以按指定的一列或多列值排序(字典中的拼音索引)(物理存储顺序)
--非聚集索引：具有独立于数据行的结构，包含非聚集索引键值，且每个键值项都有指向包含该键值的数据行的指针。
--（字典中的偏旁部首索引）（逻辑存储顺序）
